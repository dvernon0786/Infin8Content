-- Get complete schema for keywords table
SELECT 
    column_name,
    data_type,
    is_nullable,
    column_default,
    character_maximum_length,
    numeric_precision,
    numeric_scale
FROM information_schema.columns 
WHERE table_name = 'keywords' 
AND table_schema = 'public'
ORDER BY ordinal_position;

-- Get check constraints for keywords table
SELECT 
    conname,
    pg_get_constraintdef(oid) as consrc,
    contype
FROM pg_constraint 
WHERE conrelid::regclass = 'keywords'::regclass
AND contype = 'c'
ORDER BY conname;

-- Get foreign key constraints for keywords table
SELECT
    tc.constraint_name, 
    tc.constraint_type, 
    kcu.column_name, 
    ccu.table_name AS foreign_table_name,
    ccu.column_name AS foreign_column_name 
FROM information_schema.table_constraints AS tc 
JOIN information_schema.key_column_usage AS kcu
  ON tc.constraint_name = kcu.constraint_name
  AND tc.table_schema = kcu.table_schema
JOIN information_schema.constraint_column_usage AS ccu
  ON ccu.constraint_name = tc.constraint_name
  AND ccu.table_schema = tc.table_schema
WHERE tc.constraint_type = 'FOREIGN KEY' 
  AND tc.table_name = 'keywords';
