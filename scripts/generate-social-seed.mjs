#!/usr/bin/env node
/**
 * scripts/generate-social-seed.mjs
 *
 * Calls the Outstand API to list your connected social accounts, then
 * prints a ready-to-run SQL snippet for seed-social-accounts.sql.
 *
 * Usage:
 *   OUTSTAND_API_KEY=sk_live_xxx node scripts/generate-social-seed.mjs
 *
 * It also prints the SQL to look up your org_id from Supabase.
 */

const API_KEY = process.env.OUTSTAND_API_KEY

if (!API_KEY) {
  console.error('Error: OUTSTAND_API_KEY environment variable is not set.')
  console.error('  export OUTSTAND_API_KEY=sk_live_xxx')
  process.exit(1)
}

async function main() {
  console.log('Fetching social accounts from OutstandвЂ¦\n')

  let accounts
  try {
    const res = await fetch('https://api.outstand.so/v1/social-accounts', {
      headers: { Authorization: `Bearer ${API_KEY}` },
    })
    const json = await res.json()
    if (!res.ok || json.success === false) {
      throw new Error(json.error ?? json.message ?? `HTTP ${res.status}`)
    }
    accounts = json.data ?? []
  } catch (err) {
    console.error('Outstand API call failed:', err.message)
    process.exit(1)
  }

  if (accounts.length === 0) {
    console.log('No social accounts connected to this Outstand key.')
    console.log('Connect accounts at https://app.outstand.so/settings/social')
    process.exit(0)
  }

  console.log(`Found ${accounts.length} account(s):\n`)
  console.table(accounts.map(a => ({ id: a.id, network: a.network, username: a.username })))

  // в”Ђв”Ђ Step 1: org_id lookup в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ

  console.log('\nв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ')
  console.log('STEP 1 вЂ” run this in Supabase SQL Editor to get your org_id:')
  console.log('в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ')
  console.log('  SELECT id, name FROM organizations;\n')

  // в”Ђв”Ђ Step 2: ready-to-run seed SQL в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ

  const inserts = accounts.map(a => {
    const net = a.network.toLowerCase()
    const user = (a.username ?? a.nickname ?? '').replace(/'/g, "''")
    return `  INSERT INTO org_social_accounts (organization_id, outstand_account_id, network, username, is_active)
  VALUES (v_org_id, '${a.id}', '${net}', '${user}', TRUE)
  ON CONFLICT (organization_id, outstand_account_id) DO UPDATE
    SET is_active = TRUE, username = EXCLUDED.username, updated_at = NOW();`
  }).join('\n\n')

  const sql = `\
-- ============================================================
-- Generated by scripts/generate-social-seed.mjs
-- ============================================================
-- Fill in YOUR org_id from Step 1, then run in Supabase SQL Editor.

DO $$
DECLARE
  v_org_id UUID := '<paste-your-org-id-here>';   -- в†ђ from Step 1
BEGIN

${inserts}

  RAISE NOTICE 'org_social_accounts seeded for org %', v_org_id;
END $$;

-- Verify:
SELECT outstand_account_id, network, username, is_active
FROM org_social_accounts
WHERE organization_id = '<paste-your-org-id-here>';
`

  console.log('в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ')
  console.log('STEP 2 вЂ” paste your org_id from Step 1, then run this SQL:')
  console.log('в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ\n')
  console.log(sql)
}

main()
